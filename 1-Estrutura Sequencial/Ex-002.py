# Entrada das notas
nota1 = float(input('Primeira nota:'))
nota2 = float(input('Segunda nota:'))
nota3 = float(input('Terçeira nota:'))

# Média das notas
media = (nota1 + nota2 + nota3) / 3

# Mostra a média aritimética
print('Média aritimética das notas:{}'.format(media))