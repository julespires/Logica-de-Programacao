# Entrada de dados
num1 = int(input("Primeiro valor:"))
num2 = int(input("Segundo valor:"))
num3 = int(input("Terçeiro valor:"))
num4 = int(input("Quarto valor:"))

# Calcula a soma
soma = num1 + num2 + num3 + num4


# Mostra a soma
print("Soma:{}".format(soma))